void play(uint8_t samples[], int length) {

  // Use timer 2 to generate an analog voltage using PWM on pin 11
  pinMode(11, OUTPUT);
  TCCR2A |= bit(COM2A1);
  TCCR2B = bit(CS20);

  // Shine the status LED while we are playing
  pinMode(LED_BUILTIN, OUTPUT);
  digitalWrite(LED_BUILTIN, HIGH);

  // Send each sample to the PWM and delay to achieve 2.5 kHz
  for (int i = 0; i < length; i++) {
    uint8_t value = pgm_read_byte(&samples[i]);
    OCR2A = value;
    delayMicroseconds(120); // adjust to customize speed
  }

  digitalWrite(LED_BUILTIN, LOW);
}

void play_point() {
  play(AUDIO_SAMPLES_POINT, sizeof(AUDIO_SAMPLES_POINT));
}

void play_digit(uint8_t digit) {
  switch (digit){
    case 0:
      play(AUDIO_SAMPLES_ZERO, sizeof(AUDIO_SAMPLES_ZERO));
      break;
    case 1:
      play(AUDIO_SAMPLES_ONE, sizeof(AUDIO_SAMPLES_ONE));
      break;
    case 2:
      play(AUDIO_SAMPLES_TWO, sizeof(AUDIO_SAMPLES_TWO));
      break;
    case 3:
      play(AUDIO_SAMPLES_THREE, sizeof(AUDIO_SAMPLES_THREE));
      break;
    case 4:
      play(AUDIO_SAMPLES_FOUR, sizeof(AUDIO_SAMPLES_FOUR));
      break;
    case 5:
      play(AUDIO_SAMPLES_FIVE, sizeof(AUDIO_SAMPLES_FIVE));
      break;
    case 6:
      play(AUDIO_SAMPLES_SIX, sizeof(AUDIO_SAMPLES_SIX));
      break;
    case 7:
      play(AUDIO_SAMPLES_SEVEN, sizeof(AUDIO_SAMPLES_SEVEN));
      break;
    case 8:
      play(AUDIO_SAMPLES_EIGHT, sizeof(AUDIO_SAMPLES_EIGHT));
      break;
    case 9:
      play(AUDIO_SAMPLES_NINE, sizeof(AUDIO_SAMPLES_NINE));
      break;
    default:
      break;
  }
}

